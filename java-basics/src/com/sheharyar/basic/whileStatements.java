package com.sheharyar.basic;

public class whileStatements {

    public static void main(String[] args ) {

        //While statement
        int count = 1;
        while (count < 10) {
            System.out.println(count);
            count++;
        }
        // Do While Statement
        count = 1;
        do {
            System.out.println(count);
            count ++;
        } while (count < 10);
    }
}
